// ── Helper Functions ──

/** Replace {product} and {product_description} placeholders in a template prompt */
export function hydratePrompt(
  template: string,
  productName: string,
  productDescription?: string
): string {
  let result = template.replace(/\{product\}/g, productName)
  result = result.replace(
    /\{product_description\}/g,
    productDescription || `A premium ${productName}`
  )
  return result
}

/** Calculate total credit cost for a template (Standard model = 2 credits/sec).
 *  Pass overrideDurationSeconds to recalculate for a user-chosen duration. */
export function calculateTemplateCreditCost(
  template: ShotTemplate,
  overrideDurationSeconds?: number
): number {
  const STANDARD_RATE = 2 // credits per second
  const duration = overrideDurationSeconds ?? template.totalDurationSeconds
  return duration * STANDARD_RATE
}

/** Get templates by category */
export function getTemplatesByCategory(category: TemplateCategory): ShotTemplate[] {
  return SHOT_TEMPLATES.filter((t) => t.category === category)
}

/** Get a template by ID */
export function getTemplateById(id: string): ShotTemplate | undefined {
  return SHOT_TEMPLATES.find((t) => t.id === id)
}

/** Search templates by query string */
export function searchTemplates(query: string): ShotTemplate[] {
  const q = query.toLowerCase().trim()
  if (!q) return SHOT_TEMPLATES
  return SHOT_TEMPLATES.filter(
    (t) =>
      t.name.toLowerCase().includes(q) ||
      t.description.toLowerCase().includes(q) ||
      t.tags.some((tag) => tag.toLowerCase().includes(q))
  )
}

/** Get templates by duration tier */
export function getTemplatesByDurationTier(tier: TemplateDurationTier): ShotTemplate[] {
  return SHOT_TEMPLATES.filter((t) => t.durationTier === tier)
}
